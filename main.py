                      logger.warning(f"ChatID: {chat_id} | –û—à–∏–±–∫–∞ –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –∏–Ω—Ñ–æ –∏–∑ –ø—É—Å—Ç–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ (—Ñ–æ—Ç–æ): {e_inner}", exc_info=True)
                      reply = "ü§ñ –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–Ω—è—Ç—å, —á—Ç–æ –Ω–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏ (–æ—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ—Ç–≤–µ—Ç–∞)."
