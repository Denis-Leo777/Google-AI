async def setup_bot_and_server(stop_event: asyncio.Event):
    """–ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ—Ç –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –±–æ—Ç–∞, –≤–µ–±—Ö—É–∫ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –∏ –∫–æ—Ä—É—Ç–∏–Ω—É –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞."""
    application = Application.builder().token(TELEGRAM_BOT_TOKEN).build()

    # –ù–∞—Å—Ç—Ä–æ–π–∫–∞ HTTP-—Å–µ—Å—Å–∏–∏ –¥–ª—è –±–æ—Ç–∞ (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø–æ–∏—Å–∫–∞)
    timeout = aiohttp.ClientTimeout(total=60.0, connect=10.0, sock_connect=10.0, sock_read=30.0)
    aiohttp_session = aiohttp.ClientSession(timeout=timeout)
    application.bot_data['aiohttp_session'] = aiohttp_session
    logger.info("–°–µ—Å—Å–∏—è aiohttp —Å–æ–∑–¥–∞–Ω–∞ –∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞ –≤ bot_data.")

    # –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤
    application.add_handler(CommandHandler("start", start))
    application.add_handler(CommandHandler("model", model_command))
    application.add_handler(CommandHandler("clear", clear_history))
    application.add_handler(CommandHandler("temp", set_temperature))
    application.add_handler(CommandHandler("search_on", enable_search))
    application.add_handler(CommandHandler("search_off", disable_search))
    application.add_handler(CallbackQueryHandler(select_model_callback, pattern="^set_model_"))
    application.add_handler(MessageHandler(filters.PHOTO, handle_photo))
    application.add_handler(MessageHandler(filters.Document.ALL, handle_document))
    application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    try:
        await application.initialize()
        webhook_path_segment = GEMINI_WEBHOOK_PATH.strip('/')
        # <<<<<<<<<<<<<<<<<<<<<<<<<<<<<< –ò–ó–ú–ï–ù–ï–ù–ò–ï: –ê–∫–∫—É—Ä–∞—Ç–Ω–æ–µ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–µ URL <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
        # –§–æ—Ä–º–∏—Ä—É–µ–º URL –±–µ–∑ urljoin, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –¥–≤–æ–π–Ω—ã—Ö —Å–ª—ç—à–µ–π –µ—Å–ª–∏ WEBHOOK_HOST –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ /
        webhook_url = f"{WEBHOOK_HOST.rstrip('/')}/{webhook_path_segment}"
        # ============================================================================================
        logger.info(f"–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤–µ–±—Ö—É–∫–∞: {webhook_url}")
        await application.bot.set_webhook(
            url=webhook_url,
            allowed_updates=Update.ALL_TYPES,
            drop_pending_updates=True, # –°–±—Ä–∞—Å—ã–≤–∞–µ–º –æ–∂–∏–¥–∞—é—â–∏–µ –∞–ø–¥–µ–π—Ç—ã –ø—Ä–∏ —Ä–µ—Å—Ç–∞—Ä—Ç–µ
            secret_token=os.getenv('WEBHOOK_SECRET_TOKEN') # –î–æ–±–∞–≤–ª–µ–Ω–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∞ —Å–µ–∫—Ä–µ—Ç–Ω–æ–≥–æ —Ç–æ–∫–µ–Ω–∞
        )
        logger.info("–í–µ–±—Ö—É–∫ —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω.")
        return application, run_web_server(application, stop_event)
    except Exception as e:
        logger.critical(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –±–æ—Ç–∞ –∏–ª–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ –≤–µ–±—Ö—É–∫–∞: {e}", exc_info=True)
        # –£–±–µ–¥–∏–º—Å—è, —á—Ç–æ —Å–µ—Å—Å–∏—è –∑–∞–∫—Ä—ã—Ç–∞ –ø—Ä–∏ –æ—à–∏–±–∫–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏
        if 'aiohttp_session' in application.bot_data and application.bot_data['aiohttp_session'] and not application.bot_data['aiohttp_session'].closed:
             await application.bot_data['aiohttp_session'].close()
             logger.info("–°–µ—Å—Å–∏—è aiohttp –∑–∞–∫—Ä—ã—Ç–∞ –∏–∑-–∑–∞ –æ—à–∏–±–∫–∏ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏.")
        raise


async def run_web_server(application: Application, stop_event: asyncio.Event):
    """–ó–∞–ø—É—Å–∫–∞–µ—Ç –≤–µ–±-—Å–µ—Ä–≤–µ—Ä aiohttp –¥–ª—è –ø—Ä–∏–µ–º–∞ –≤–µ–±—Ö—É–∫–æ–≤."""
    app = aiohttp.web.Application()

    async def health_check(request):
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å –±–æ—Ç–∞."""
        try:
            # –ü—Ä–æ—Å—Ç–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ - –ø–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –±–æ—Ç–µ
            bot_info = await application.bot.get_me()
            if bot_info:
                 # –ú–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ø—Ä–æ–≤–µ—Ä–∫—É —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è —Å Gemini, –Ω–æ —ç—Ç–æ –º–æ–∂–µ—Ç –∑–∞–º–µ–¥–ª—è—Ç—å health check
                 # try:
                 #    model = genai.GenerativeModel(DEFAULT_MODEL)
                 #    await asyncio.to_thread(model.count_tokens, "health check")
                 #    gemini_status = "OK"
                 # except Exception as gemini_e:
                 #    gemini_status = f"Error ({type(gemini_e).__name__})"
                 #    logger.warning(f"Health check: Gemini connection test failed: {gemini_e}")

                 return aiohttp.web.Response(text=f"OK: Bot {bot_info.username} is running.") # Gemini status: {gemini_status}
            else:
                 logger.warning("Health check: Bot info not available from get_me()")
                 return aiohttp.web.Response(text="Error: Bot info not available", status=503)
        except Exception as e:
            logger.error(f"Health check failed: {e}", exc_info=True) # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É
            return aiohttp.web.Response(text=f"Error: Health check failed ({type(e).__name__})", status=503)

    app.router.add_get('/', health_check) # Health check –Ω–∞ –∫–æ—Ä–Ω–µ–≤–æ–º –ø—É—Ç–∏
    app['bot_app'] = application # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –±–æ—Ç–∞ –≤ –∫–æ–Ω—Ç–µ–∫—Å—Ç–µ aiohttp

    # –£–±–µ–¥–∏–º—Å—è, —á—Ç–æ –ø—É—Ç—å –≤–µ–±—Ö—É–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å–æ —Å–ª—ç—à–∞
    webhook_path = GEMINI_WEBHOOK_PATH.strip('/')
    if not webhook_path.startswith('/'):
         webhook_path = '/' + webhook_path
    app.router.add_post(webhook_path, handle_telegram_webhook) # –û—Å–Ω–æ–≤–Ω–æ–π –ø—É—Ç—å –¥–ª—è –≤–µ–±—Ö—É–∫–æ–≤
    logger.info(f"–í–µ–±—Ö—É–∫ –±—É–¥–µ—Ç —Å–ª—É—à–∞—Ç—å –Ω–∞ –ø—É—Ç–∏: {webhook_path}")

    runner = aiohttp.web.AppRunner(app)
    await runner.setup()

    port = int(os.getenv("PORT", "10000")) # –ò—Å–ø–æ–ª—å–∑—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –æ–∫—Ä—É–∂–µ–Ω–∏—è PORT –∏–ª–∏ 10000
    host = os.getenv("HOST", "0.0.0.0") # –°–ª—É—à–∞–µ–º –Ω–∞ –≤—Å–µ—Ö –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞—Ö –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
    site = aiohttp.web.TCPSite(runner, host, port)
    try:
        await site.start()
        logger.info(f"–í–µ–±-—Å–µ—Ä–≤–µ—Ä –∑–∞–ø—É—â–µ–Ω –Ω–∞ http://{host}:{port}")
        # –û–∂–∏–¥–∞–µ–º —Å–æ–±—ã—Ç–∏—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
        await stop_event.wait()
    except asyncio.CancelledError:
         logger.info("–ó–∞–¥–∞—á–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞.")
    except Exception as e:
        # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫–∏ –ø—Ä–∏ —Å—Ç–∞—Ä—Ç–µ —Å–µ—Ä–≤–µ—Ä–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–æ—Ä—Ç –∑–∞–Ω—è—Ç)
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ/—Ä–∞–±–æ—Ç–µ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –Ω–∞ {host}:{port}: {e}", exc_info=True)
    finally:
        logger.info("–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞...")
        await runner.cleanup() # –ö–æ—Ä—Ä–µ–∫—Ç–Ω–æ –æ—Å–≤–æ–±–æ–∂–¥–∞–µ–º —Ä–µ—Å—É—Ä—Å—ã
        logger.info("–í–µ–±-—Å–µ—Ä–≤–µ—Ä –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω.")


async def handle_telegram_webhook(request: aiohttp.web.Request) -> aiohttp.web.Response:
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –≤—Ö–æ–¥—è—â–∏–µ –∑–∞–ø—Ä–æ—Å—ã –æ—Ç Telegram."""
    application = request.app.get('bot_app')
    if not application:
        logger.critical("–û–±—ä–µ–∫—Ç –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –±–æ—Ç–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –∫–æ–Ω—Ç–µ–∫—Å—Ç–µ aiohttp!")
        return aiohttp.web.Response(status=500, text="Internal Server Error: Bot application not found")

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–µ–∫—Ä–µ—Ç–Ω–æ–≥–æ —Ç–æ–∫–µ–Ω–∞, –µ—Å–ª–∏ –æ–Ω —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω
    secret_token = os.getenv('WEBHOOK_SECRET_TOKEN')
    if secret_token:
         header_token = request.headers.get('X-Telegram-Bot-Api-Secret-Token')
         if header_token != secret_token:
             logger.warning("–ü–æ–ª—É—á–µ–Ω –∑–∞–ø—Ä–æ—Å —Å –Ω–µ–≤–µ—Ä–Ω—ã–º —Å–µ–∫—Ä–µ—Ç–Ω—ã–º —Ç–æ–∫–µ–Ω–æ–º.")
             # –í–æ–∑–≤—Ä–∞—â–∞–µ–º 403 Forbidden
             return aiohttp.web.Response(status=403, text="Forbidden: Invalid secret token")

    try:
        data = await request.json()
        update = Update.de_json(data, application.bot)
        # <<<<<<<<<<<<<<<<<<<<<<<<<<<<<< –ò–°–ü–†–ê–í–õ–ï–ù–ò–ï: –£–±—Ä–∞–ª–∏ create_task –∏ shield <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
        # –ü—Ä–æ—Å—Ç–æ –∂–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∞–ø–¥–µ–π—Ç–∞
        await application.process_update(update)
        # ============================================================================================
        # –û—Ç–≤–µ—á–∞–µ–º Telegram, —á—Ç–æ –≤—Å–µ –û–ö
        return aiohttp.web.Response(text="OK", status=200)
    except json.JSONDecodeError as e:
         # –ï—Å–ª–∏ Telegram –ø—Ä–∏—Å–ª–∞–ª –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π JSON
         body = await request.text() # –ß–∏—Ç–∞–µ–º —Ç–µ–ª–æ –∑–∞–ø—Ä–æ—Å–∞ –¥–ª—è –ª–æ–≥–∞
         logger.error(f"–û—à–∏–±–∫–∞ –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞–Ω–∏—è JSON –æ—Ç Telegram: {e}. –¢–µ–ª–æ: {body[:500]}...")
         return aiohttp.web.Response(text="Bad Request", status=400)
    except Exception as e:
        # –õ–æ–≤–∏–º –¥—Ä—É–≥–∏–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –æ—à–∏–±–∫–∏ –ø—Ä–∏ –¥–µ—Å–µ—Ä–∏–∞–ª–∏–∑–∞—Ü–∏–∏ Update –∏–ª–∏ –≤–æ –≤—Ä–µ–º—è process_update
        logger.error(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –≤–µ–±—Ö—É–∫-–∑–∞–ø—Ä–æ—Å–∞: {e}", exc_info=True)
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º 500, —á—Ç–æ–±—ã Telegram –ø–æ–ø—Ä–æ–±–æ–≤–∞–ª —Å–Ω–æ–≤–∞ (–µ—Å–ª–∏ —ç—Ç–æ –≤—Ä–µ–º–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞)
        return aiohttp.web.Response(text="Internal Server Error", status=500)


async def main():
    """–û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è."""
    # –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —É—Ä–æ–≤–Ω–µ–π –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
    log_level_str = os.getenv("LOG_LEVEL", "INFO").upper()
    log_level = getattr(logging, log_level_str, logging.INFO)
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –±–∞–∑–æ–≤—É—é –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é - —É—Ä–æ–≤–µ–Ω—å INFO –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
    logging.basicConfig(
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
        level=logging.INFO, # –ù–∞—á–∏–Ω–∞–µ–º —Å INFO
        # –ú–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å —Ö–µ–Ω–¥–ª–µ—Ä—ã –¥–ª—è –≤—ã–≤–æ–¥–∞ –≤ —Ñ–∞–π–ª –∏ —Ç.–¥.
        # handlers=[logging.StreamHandler(), logging.FileHandler("bot.log")]
    )

    # <<<<<<<<<<<<<<<<<<<<<<<<<<<<<< –ò–ó–ú–ï–ù–ï–ù–ò–ï: –ö–æ—Ä—Ä–µ–∫—Ç–∏—Ä–æ–≤–∫–∞ —É—Ä–æ–≤–Ω–µ–π –ª–æ–≥–æ–≤ <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
    # –ü–æ–Ω–∏–∂–∞–µ–º —É—Ä–æ–≤–µ–Ω—å –¥–ª—è —Å–ª–∏—à–∫–æ–º "–±–æ–ª—Ç–ª–∏–≤—ã—Ö" –±–∏–±–ª–∏–æ—Ç–µ–∫
    logging.getLogger('httpx').setLevel(logging.WARNING)
    logging.getLogger('httpcore').setLevel(logging.WARNING)
    logging.getLogger('google.api_core').setLevel(logging.WARNING)
    # –û—Å—Ç–∞–≤–ª—è–µ–º INFO –¥–ª—è Gemini –∏ DDG –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ –ø–æ–∏—Å–∫–∞ –∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏
    logging.getLogger('google.generativeai').setLevel(logging.INFO)
    logging.getLogger('duckduckgo_search').setLevel(logging.INFO)
    # PIL –∏ Tesseract –º–æ–≥—É—Ç –±—ã—Ç—å —à—É–º–Ω—ã–º–∏, —Å—Ç–∞–≤–∏–º INFO –∏–ª–∏ WARNING
    logging.getLogger('PIL').setLevel(logging.INFO)
    logging.getLogger('pytesseract').setLevel(logging.INFO)
    # –õ–æ–≥–∏ –¥–æ—Å—Ç—É–ø–∞ aiohttp –º–æ–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å WARNING –≤ –ø—Ä–æ–¥–µ
    logging.getLogger('aiohttp.access').setLevel(logging.WARNING)
    # –£—Ä–æ–≤–µ–Ω—å –¥–ª—è PTB –º–æ–∂–Ω–æ –æ—Å—Ç–∞–≤–∏—Ç—å INFO –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ –∏–ª–∏ WARNING –≤ –ø—Ä–æ–¥–∞–∫—à–µ–Ω–µ
    logging.getLogger('telegram.ext').setLevel(logging.INFO)
    logging.getLogger('telegram.bot').setLevel(logging.INFO) # –õ–æ–≥–∏ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏—è —Å API Telegram
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —É—Ä–æ–≤–µ–Ω—å –¥–ª—è –Ω–∞—à–µ–≥–æ –ª–æ–≥–≥–µ—Ä–∞, –∫–æ—Ç–æ—Ä—ã–π –º—ã –∑–∞–¥–∞–ª–∏ —á–µ—Ä–µ–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –æ–∫—Ä—É–∂–µ–Ω–∏—è
    logger.setLevel(log_level)
    logger.info(f"--- –£—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω —É—Ä–æ–≤–µ–Ω—å –ª–æ–≥–≥–∏—Ä–æ–≤–∞–Ω–∏—è: {log_level_str} ({log_level}) ---")
    # ============================================================================================


    loop = asyncio.get_running_loop()
    stop_event = asyncio.Event() # –°–æ–±—ã—Ç–∏–µ –¥–ª—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ü–∏–∏ –æ—Å—Ç–∞–Ω–æ–≤–∫–∏

    # –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–∏–≥–Ω–∞–ª–æ–≤ SIGINT (Ctrl+C) –∏ SIGTERM (–æ—Ç Docker/systemd)
    def signal_handler():
        if not stop_event.is_set():
             logger.info("–ü–æ–ª—É—á–µ–Ω —Å–∏–≥–Ω–∞–ª SIGINT/SIGTERM, –∏–Ω–∏—Ü–∏–∏—Ä—É—é —à—Ç–∞—Ç–Ω—É—é –æ—Å—Ç–∞–Ω–æ–≤–∫—É...")
             stop_event.set() # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å–æ–±—ã—Ç–∏–µ, —á—Ç–æ–±—ã –≥–ª–∞–≤–Ω—ã–π —Ü–∏–∫–ª –∏ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä –Ω–∞—á–∞–ª–∏ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—Ç—å—Å—è
        else:
             logger.warning("–ü–æ–≤—Ç–æ—Ä–Ω—ã–π —Å–∏–≥–Ω–∞–ª –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ –ø–æ–ª—É—á–µ–Ω. –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —É–∂–µ –∏–¥–µ—Ç.")

    # –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–∏–≥–Ω–∞–ª–æ–≤ –≤ —Ü–∏–∫–ª —Å–æ–±—ã—Ç–∏–π
    for sig in (signal.SIGINT, signal.SIGTERM):
        try:
            # –ü—Ä–µ–¥–ø–æ—á—Ç–∏—Ç–µ–ª—å–Ω—ã–π —Å–ø–æ—Å–æ–± –¥–ª—è asyncio
            loop.add_signal_handler(sig, signal_handler)
        except NotImplementedError:
             # Fallback –¥–ª—è —Å–∏—Å—Ç–µ–º, –≥–¥–µ add_signal_handler –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, Windows –≤ –Ω–µ–∫–æ—Ç–æ—Ä—ã—Ö —Å–ª—É—á–∞—è—Ö)
             logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è {sig} —á–µ—Ä–µ–∑ loop.add_signal_handler. –ò—Å–ø–æ–ª—å–∑—É—é signal.signal().")
             try:
                 # –≠—Ç–æ—Ç —Å–ø–æ—Å–æ–± –º–æ–∂–µ—Ç –±—ã—Ç—å –Ω–µ –∏–¥–µ–∞–ª—å–Ω—ã–º –≤ asyncio, –Ω–æ –ª—É—á—à–µ, —á–µ–º –Ω–∏—á–µ–≥–æ
                 signal.signal(sig, lambda s, f: signal_handler())
             except Exception as e_signal:
                 logger.error(f"–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ {sig} —á–µ—Ä–µ–∑ signal.signal: {e_signal}")


    application = None
    web_server_task = None
    aiohttp_session_main = None # –•—Ä–∞–Ω–∏–º —Å–µ—Å—Å–∏—é –∑–¥–µ—Å—å –¥–ª—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–≥–æ –∑–∞–∫—Ä—ã—Ç–∏—è

    try:
        logger.info(f"--- –ó–∞–ø—É—Å–∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è Gemini Telegram Bot ---")
        application, web_server_coro = await setup_bot_and_server(stop_event)
        # –ó–∞–ø—É—Å–∫–∞–µ–º –≤–µ–±-—Å–µ—Ä–≤–µ—Ä –∫–∞–∫ –∑–∞–¥–∞—á—É asyncio
        web_server_task = asyncio.create_task(web_server_coro)
        # –ü–æ–ª—É—á–∞–µ–º —Å–µ—Å—Å–∏—é –∏–∑ bot_data –¥–ª—è –ø–æ—Å–ª–µ–¥—É—é—â–µ–≥–æ –∑–∞–∫—Ä—ã—Ç–∏—è
        aiohttp_session_main = application.bot_data.get('aiohttp_session')

        logger.info("–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–æ –∏ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä –∑–∞–ø—É—â–µ–Ω. –û–∂–∏–¥–∞–Ω–∏–µ —Å–∏–≥–Ω–∞–ª–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ (Ctrl+C)...")
        # –ì–ª–∞–≤–Ω—ã–π —Ü–∏–∫–ª –æ–∂–∏–¥–∞–µ—Ç —Å–æ–±—ã—Ç–∏—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
        await stop_event.wait()

    except asyncio.CancelledError:
        # –ï—Å–ª–∏ –≥–ª–∞–≤–Ω–∞—è –∑–∞–¥–∞—á–∞ –±—ã–ª–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞ –∏–∑–≤–Ω–µ (–º–∞–ª–æ–≤–µ—Ä–æ—è—Ç–Ω–æ)
        logger.info("–ì–ª–∞–≤–Ω–∞—è –∑–∞–¥–∞—á–∞ main() –±—ã–ª–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞.")
    except Exception as e:
        # –õ–æ–≤–∏–º –∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏–µ –æ—à–∏–±–∫–∏ –Ω–∞ —ç—Ç–∞–ø–µ –∑–∞–ø—É—Å–∫–∞
        logger.critical("–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –≤ –≥–ª–∞–≤–Ω–æ–º –ø–æ—Ç–æ–∫–µ –¥–æ –∏–ª–∏ –≤–æ –≤—Ä–µ–º—è –æ–∂–∏–¥–∞–Ω–∏—è.", exc_info=True)
    finally:
        logger.info("--- –ù–∞—á–∞–ª–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ —à—Ç–∞—Ç–Ω–æ–π –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è ---")

        # –£–±–µ–¥–∏–º—Å—è, —á—Ç–æ —Å–æ–±—ã—Ç–∏–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ, –¥–∞–∂–µ –µ—Å–ª–∏ –æ—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏–Ω–∏—Ü–∏–∏—Ä–æ–≤–∞–Ω–∞ –∏–Ω–∞—á–µ
        if not stop_event.is_set(): stop_event.set()

        # 1. –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≤–µ–±-—Å–µ—Ä–≤–µ—Ä, —á—Ç–æ–±—ã –æ–Ω –ø–µ—Ä–µ—Å—Ç–∞–ª –ø—Ä–∏–Ω–∏–º–∞—Ç—å –Ω–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã
        if web_server_task and not web_server_task.done():
             logger.info("–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ (graceful shutdown)...")
             # –í–µ–±-—Å–µ—Ä–≤–µ—Ä –¥–æ–ª–∂–µ–Ω —Å–∞–º –∑–∞–≤–µ—Ä—à–∏—Ç—å—Å—è –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ stop_event, –Ω–æ –¥–∞–¥–∏–º –µ–º—É –≤—Ä–µ–º—è
             try:
                 # –ñ–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –∑–∞–¥–∞—á–∏ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ —Å —Ç–∞–π–º–∞—É—Ç–æ–º
                 await asyncio.wait_for(web_server_task, timeout=15.0)
                 logger.info("–í–µ–±-—Å–µ—Ä–≤–µ—Ä —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–µ–Ω.")
             except asyncio.TimeoutError:
                 logger.warning("–í–µ–±-—Å–µ—Ä–≤–µ—Ä –Ω–µ –∑–∞–≤–µ—Ä—à–∏–ª—Å—è –∑–∞ 15 —Å–µ–∫—É–Ω–¥, –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–∞—è –æ—Ç–º–µ–Ω–∞ –∑–∞–¥–∞—á–∏...")
                 web_server_task.cancel()
                 try: await web_server_task # –ñ–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –æ—Ç–º–µ–Ω—ã
                 except asyncio.CancelledError: logger.info("–ó–∞–¥–∞—á–∞ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –æ—Ç–º–µ–Ω–µ–Ω–∞.")
                 except Exception as e_cancel: logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–∂–∏–¥–∞–Ω–∏–∏ –æ—Ç–º–µ–Ω—ã –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞: {e_cancel}", exc_info=True)
             except asyncio.CancelledError:
                  # –ï—Å–ª–∏ —Å–∞–º–æ –æ–∂–∏–¥–∞–Ω–∏–µ –±—ã–ª–æ –æ—Ç–º–µ–Ω–µ–Ω–æ
                  logger.info("–û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –±—ã–ª–æ –æ—Ç–º–µ–Ω–µ–Ω–æ.")
             except Exception as e_wait:
                  # –î—Ä—É–≥–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–∂–∏–¥–∞–Ω–∏–∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
                  logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–∂–∏–¥–∞–Ω–∏–∏ —à—Ç–∞—Ç–Ω–æ–≥–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞: {e_wait}", exc_info=True)

        # 2. –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ Telegram –±–æ—Ç–∞ (–∑–∞–∫—Ä—ã–≤–∞–µ—Ç —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è, –∏ —Ç.–¥.)
        if application:
            logger.info("–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è Telegram –±–æ—Ç–∞ (application.shutdown)...")
            try:
                 await application.shutdown()
                 logger.info("–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ Telegram –±–æ—Ç–∞ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ.")
            except Exception as e_shutdown:
                 logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ application.shutdown(): {e_shutdown}", exc_info=True)

        # 3. –ó–∞–∫—Ä—ã–≤–∞–µ–º HTTP —Å–µ—Å—Å–∏—é, —Å–æ–∑–¥–∞–Ω–Ω—É—é –≤ main/setup
        if aiohttp_session_main and not aiohttp_session_main.closed:
             logger.info("–ó–∞–∫—Ä—ã—Ç–∏–µ –æ—Å–Ω–æ–≤–Ω–æ–π —Å–µ—Å—Å–∏–∏ aiohttp...")
             await aiohttp_session_main.close()
             # –ù–µ–±–æ–ª—å—à–∞—è –ø–∞—É–∑–∞ –¥–ª—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è —Ñ–æ–Ω–æ–≤—ã—Ö –∑–∞–¥–∞—á –∑–∞–∫—Ä—ã—Ç–∏—è —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–π
             await asyncio.sleep(0.5)
             logger.info("–û—Å–Ω–æ–≤–Ω–∞—è —Å–µ—Å—Å–∏—è aiohttp –∑–∞–∫—Ä—ã—Ç–∞.")

        # 4. –û—Ç–º–µ–Ω—è–µ–º –≤—Å–µ –µ—â–µ —Ä–∞–±–æ—Ç–∞—é—â–∏–µ –∑–∞–¥–∞—á–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, process_update)
        # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –∑–∞–¥–∞—á–∏, –∫—Ä–æ–º–µ —Ç–µ–∫—É—â–µ–π (main)
        tasks = [t for t in asyncio.all_tasks() if t is not asyncio.current_task()]
        if tasks:
            logger.info(f"–û—Ç–º–µ–Ω–∞ {len(tasks)} –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è —Ñ–æ–Ω–æ–≤—ã—Ö –∑–∞–¥–∞—á...")
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–∏–≥–Ω–∞–ª –æ—Ç–º–µ–Ω—ã –≤—Å–µ–º –∑–∞–¥–∞—á–∞–º
            [task.cancel() for task in tasks]
            # –°–æ–±–∏—Ä–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –æ—Ç–º–µ–Ω—ã (–∂–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è)
            # return_exceptions=True, —á—Ç–æ–±—ã gather –Ω–µ —É–ø–∞–ª –ø—Ä–∏ –ø–µ—Ä–≤–æ–π –∂–µ CancelledError
            results = await asyncio.gather(*tasks, return_exceptions=True)
            # –õ–æ–≥–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã/–æ—à–∏–±–∫–∏ –æ—Ç–º–µ–Ω–µ–Ω–Ω—ã—Ö –∑–∞–¥–∞—á –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
            cancelled_count = 0
            error_count = 0
            for i, res in enumerate(results):
                 if isinstance(res, asyncio.CancelledError):
                      cancelled_count += 1
                 elif isinstance(res, Exception):
                      error_count += 1
