            response_vision = await asyncio.to_thread(
                 model.generate_content,
                 content_for_vision # –ü–µ—Ä–µ–¥–∞–µ–º —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π
            )

            if hasattr(response_vision, 'text'):
                 reply = response_vision.text
            else:
                 reply = None
                 logger.warning(f"ChatID: {chat_id} | –û—Ç–≤–µ—Ç vision –º–æ–¥–µ–ª–∏ –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç 'text' (–ø–æ–ø—ã—Ç–∫–∞ {attempt + 1}).")

            # <<<<<<<<<<<<<<<<<<<<<<<<<<<<<< –ò–ó–ú–ï–ù–ï–ù–ò–ï: –£–ª—É—á—à–µ–Ω–Ω—ã–π –∞–Ω–∞–ª–∏–∑ –ø—É—Å—Ç–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ (—Ñ–æ—Ç–æ) <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
            if not reply:
                 block_reason_str = 'N/A'
                 finish_reason_str = 'N/A'
