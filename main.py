             reply = f"‚ùå –ê–Ω–∞–ª–∏–∑ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –º–æ–¥–µ–ª—å—é. (–ü—Ä–∏—á–∏–Ω–∞: {reason_str})"
