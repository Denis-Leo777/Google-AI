                          reply = "ü§ñ –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–Ω—è—Ç—å, —á—Ç–æ –Ω–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–∏ (–ø—É—Å—Ç–æ–π –æ—Ç–≤–µ—Ç)."
                          break # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ä–µ—Ç—Ä–∞–µ–≤, –µ—Å–ª–∏ –ø—Ä–æ—Å—Ç–æ –ø—É—Å—Ç–æ–π –æ—Ç–≤–µ—Ç –±–µ–∑ –æ—à–∏–±–∫–∏

